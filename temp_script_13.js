        function initPrivacyToggle() {
            const toggle = document.getElementById('perfilPublicoToggle');
            const chipText = document.getElementById('privacyChipText');
            if (!toggle) return;

            const urlParams = new URLSearchParams(window.location.search);
            const paramUserId = urlParams.get('usuario_id');
            const usuarioIdFromStorage = localStorage.getItem('usuarioId');
            const esPerfilPropio = !paramUserId || parseInt(paramUserId, 10) === parseInt(usuarioIdFromStorage, 10);
            if (!esPerfilPropio) {
                const grupo = toggle.closest('.privacy-toggle-group');
                if (grupo) grupo.style.display = 'none';
                return;
            }

            const updatePrivacyUI = (esPublico) => {
                if (chipText) {
                    chipText.textContent = esPublico ? (t('publicProfile') || 'Perfil público') : (t('privateProfile') || 'Perfil privado');
                }
            };

            const esPublico = usuario.perfil_publico === true || usuario.perfil_publico_Usuario === true;
            toggle.checked = esPublico;
            updatePrivacyUI(esPublico);

            toggle.onclick = async (e) => {
                const nuevoEstado = e.target.checked;
                updatePrivacyUI(nuevoEstado);

                try {
                    const payload = { perfil_publico: nuevoEstado ? 1 : 0 };
                    const response = await fetch(`${API_BASE}/personas/${usuarioId}`, {
                        method: 'PUT',
                        headers: { 'Content-Type': 'application/json' },
                        body: JSON.stringify(payload)
                    });
                    const result = await response.json();

                    if (!response.ok || !result.success) {
                        throw new Error(result.error || result.message || 'Error al actualizar la privacidad del perfil');
                    }
                    usuario.perfil_publico = nuevoEstado;
                    if (window.Toast) {
                        Toast.success('Éxito', t('privacyUpdated') || 'Privacidad del perfil actualizada.');
                    }
                } catch (error) {
                    toggle.checked = !nuevoEstado;
                    updatePrivacyUI(!nuevoEstado);
                    if (window.Toast) {
                        Toast.error('Error', (t('privacyUpdateError') || 'No se pudo actualizar la privacidad del perfil: ') + error.message);
                    }
                }
            };
        }

        function initAvailabilityToggle() {
            const toggle = document.getElementById("availabilityToggle");

            if (!toggle) return;

            // 1. Establecer estado inicial basado en usuario cargado
            // Aseguramos compatibilidad con 1/0, true/false, "Disponible"/"No disponible"
            // Nota: El objeto usuario tiene propiedades con sufijo _Usuario (ej: disponibilidad_Usuario)
            const isAvailable = (
                usuario.disponibilidad_Usuario === 'Disponible' ||
                usuario.disponibilidad_Usuario === true ||
                usuario.disponibilidad === 'Disponible' || // fallback
                usuario.disponibilidad === true ||         // fallback
                usuario.estado === 'Disponible'
            );

            toggle.checked = isAvailable;
            updateStatusUI(isAvailable);

            // 2. Manejar cambios
            // Remover listeners anteriores si existen para evitar duplicados (usando onclick directo es mas simple aqui)
            toggle.onclick = async (e) => {
                const newStatus = e.target.checked;

                // Actualización optimista de UI
                updateStatusUI(newStatus);

                try {
                    // Backend espera "No disponible", pero UI mostrará "En obra"
                    const statusString = newStatus ? 'Disponible' : 'No disponible';
                    const payload = {
                        disponibilidad: statusString
                    };

                    console.log('Actualizando disponibilidad:', payload);

                    const response = await fetch(`${API_BASE}/personas/${usuarioId}`, {
                        method: 'PUT',
                        headers: {
                            'Content-Type': 'application/json'
                        },
                        body: JSON.stringify(payload)
                    });

                    const result = await response.json();

                    if (!response.ok || !result.success) {
                        throw new Error(result.message || 'Error al actualizar estado');
                    }

                    console.log('Estado actualizado con éxito');
                    // Actualizar objeto usuario local
                    usuario.disponibilidad_Usuario = statusString;
                    usuario.disponibilidad = statusString;

                } catch (error) {
                    console.error('Error al cambiar estado:', error);
                    // Revertir UI
                    toggle.checked = !newStatus;
                    updateStatusUI(!newStatus);
                    if (window.Toast) {
                        Toast.error('Error', 'No se pudo actualizar el estado: ' + error.message);
                    } else {
                        alert('No se pudo actualizar el estado: ' + error.message);
                    }
                }
            };
        }


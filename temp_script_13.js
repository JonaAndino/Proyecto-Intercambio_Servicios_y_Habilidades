        function actualizarFortalezaPassword() {
            const input = document.getElementById('passwordNueva');
            const fill = document.getElementById('passwordStrengthFill');
            const label = document.getElementById('passwordStrengthLabel');
            if (!input || !fill || !label) return;
            const valor = input.value;
            if (!valor) {
                fill.style.width = '0%';
                fill.style.background = '#e5e7eb';
                label.textContent = '';
                return;
            }
            let puntaje = 0;
            if (valor.length >= 6) puntaje++;
            if (valor.length >= 10) puntaje++;
            if (/[a-z]/.test(valor) && /[A-Z]/.test(valor)) puntaje++;
            if (/[0-9]/.test(valor)) puntaje++;
            if (/[^a-zA-Z0-9]/.test(valor)) puntaje++;
            
            let color = '#ef4444';
            let txt = t('passwordWeak') || 'Débil';
            if (puntaje >= 3) { color = '#f59e0b'; txt = t('passwordMedium') || 'Media'; }
            if (puntaje >= 5) { color = '#10b981'; txt = t('passwordStrong') || 'Fuerte'; }
            
            fill.style.width = (puntaje * 20) + '%';
            fill.style.background = color;
            label.textContent = txt;
            label.style.color = color;
        }

